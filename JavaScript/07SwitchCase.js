// switch Case

// Syntax 

// switch (key) {
//     case value:
//         // code to be executed 
//         break;

//     default:
//         // code to be executed 
//         break;
// }


let a = 4;


switch (a) {
    case 1:
        console.log("one");
        
        break;
    case 2:
        console.log("two");
        
        break;
    case 3:
        console.log("three");
        
        break;
    case 4:
        console.log("four");
        
        break;

    default:
        console.log("enter valid number");
        
        break;
}